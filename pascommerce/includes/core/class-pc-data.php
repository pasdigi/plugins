    /**
     * Get States/Provinces for a Country
     * Framework provides structure, Addons provide data.
     * * @param string $country_code (e.g., 'ID', 'US')
     * @return array (Code => Name)
     */
    public static function get_states( $country_code = '' ) {
        if ( empty( $country_code ) ) {
            $country_code = get_option( 'pc_store_country', 'ID' );
        }

        // Default: kosong (karena data provinsi seluruh dunia sangat besar).
        // Addon seperti "Pascommerce - RajaOngkir" akan meng-hook filter ini 
        // untuk mengisi data provinsi Indonesia secara real-time atau dari database-nya.
        $states = array();

        return apply_filters( 'pc_states', $states, $country_code );
    }

