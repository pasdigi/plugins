    /**
     * Get Currency Symbol by Code
     */
    public static function get_currency_symbol( $currency = '' ) {
        if ( ! $currency ) {
            $currency = get_option( 'pc_currency', 'IDR' );
        }

        $symbols = array(
            'IDR' => 'Rp', 'USD' => '$', 'SGD' => 'S$', 'MYR' => 'RM',
            'AUD' => '$',  'EUR' => '€', 'GBP' => '£',  'JPY' => '¥',
            'CNY' => '¥',  'KRW' => '₩', 'THB' => '฿',  'VND' => '₫',
            'PHP' => '₱',  'INR' => '₹', 'CAD' => '$',
        );

        $symbol = isset( $symbols[ $currency ] ) ? $symbols[ $currency ] : $currency;
        return apply_filters( 'pc_currency_symbol', $symbol, $currency );
    }
